@app.route('/api/locations', methods=['GET'])
def get_locations():
    """
    GET запрос - получить все локации.
    
    Когда вызывается:
    - При загрузке страницы
    - После добавления/удаления/редактирования для обновления карты
    
    Что возвращает:
    JSON массив со всеми локациями
    Пример: [{"id":1,"lat":55.75,"lon":37.61,"note":"Красная площадь",...}]
    """
    return jsonify(load_locations())


@app.route('/api/locations', methods=['POST'])
def add_location():
    """
    POST запрос - добавить новую локацию.
    
    Когда вызывается:
    При сохранении новой локации после клика по карте
    
    Что получает:
    JSON от клиента: {"lat": число, "lon": число, "note": текст, "category": текст}
    
    Что делает:
    1. Извлекает данные из запроса
    2. Загружает существующие локации
    3. Генерирует новый ID (максимальный + 1)
    4. Добавляет временную метку (текущие дата и время)
    5. Сохраняет обновленный список
    6. Возвращает подтверждение
    """
    data = request.get_json()  # Получаем JSON от браузера
    locations = load_locations()  # Загружаем текущие локации
    
    # Генерация нового ID
    # Если локации есть - берем максимальный ID и добавляем 1
    # Если нет - начинаем с 1
    if locations:
        new_id = max([loc.get('id', 0) for loc in locations]) + 1
    else:
        new_id = 1
    
    # Создаем новую локацию со всеми полями
    new_location = {
        'id': new_id,
        'lat': data['lat'],
        'lon': data['lon'],
        'note': data.get('note', ''),  # get() возвращает пустую строку, если поля нет
        'timestamp': data.get('timestamp', datetime.now().strftime('%Y-%m-%d %H:%M:%S')),
        'category': data.get('category', 'other')
    }
    
    locations.append(new_location)  # Добавляем в список
    save_locations(locations)  # Сохраняем в файл
    return jsonify({'status': 'success'})  # Отправляем подтверждение


@app.route('/api/locations/<int:location_id>', methods=['PUT'])
def update_location(location_id):
    """
    PUT запрос - обновить существующую локацию.
    
    Параметры URL:
    location_id - ID локации для редактирования
    
    Когда вызывается:
    При сохранении изменений в модальном окне редактирования
    
    Что получает:
    JSON с полями для обновления: {"note": текст, "category": текст}
    
    Что делает:
    1. Перебирает все локации в поисках нужного ID
    2. Обновляет только те поля, которые пришли от клиента
    3. Сохраняет файл
    4. Если ID не найден - возвращает ошибку 404
    """
    data = request.get_json()
    locations = load_locations()
    
    # Ищем локацию с нужным ID
    for loc in locations:
        if loc.get('id') == location_id:
            # Обновляем только переданные поля
            if 'note' in data:
                loc['note'] = data['note']
            if 'category' in data:
                loc['category'] = data['category']
            save_locations(locations)
            return jsonify({'status': 'success'})
    
    # Если не нашли - ошибка 404 (Not Found)
    return jsonify({'status': 'error', 'message': 'Location not found'}), 404


@app.route('/api/locations/<int:location_id>', methods=['DELETE'])
def delete_location(location_id):
    """
    DELETE запрос - удалить одну локацию.
    
    Параметры URL:
    location_id - ID локации для удаления
    
    Когда вызывается:
    При нажатии кнопки "Удалить" в попапе маркера
    
    Что делает:
    Создает новый список, в который входят все локации,
    КРОМЕ той, у которой ID совпадает с переданным
    (это называется фильтрация через list comprehension)
    """
    locations = load_locations()
    # Оставляем только те локации, у которых ID не равен удаляемому
    locations = [loc for loc in locations if loc.get('id') != location_id]
    save_locations(locations)
    return jsonify({'status': 'success'})


@app.route('/api/locations', methods=['DELETE'])
def delete_all_locations():
    """
    DELETE запрос - удалить ВСЕ локации.
    
    Когда вызывается:
    При нажатии кнопки "Очистить все" на боковой панели
    
    Что делает:
    Просто сохраняет пустой список в файл
    """
    save_locations([])  # Пустой список = нет локаций
    return jsonify({'status': 'success'})


