from flask import Flask, jsonify, request
# Flask - создание веб-сервера
# jsonify - превращает Python объекты в JSON ответы
# request - получает данные от клиента

import json
import os
from datetime import datetime

app = Flask(__name__)  # Создаем приложение Flask
DATA_FILE = 'locations.json'  # Имя файла для хранения локаций


# ----------------------------------------------------------------------------
# БЛОК 2: ФУНКЦИИ РАБОТЫ С ФАЙЛОМ
# ----------------------------------------------------------------------------

def load_locations():
    """
    Загружает список локаций из JSON файла.
    
    Как работает:
    1. Проверяет, существует ли файл locations.json
    2. Если да - открывает и читает содержимое
    3. Преобразует JSON в список Python словарей
    4. Для совместимости добавляет ID и категорию, если их нет
    5. Возвращает список локаций (пустой, если файла нет)
    """
    if os.path.exists(DATA_FILE):  # Проверка существования файла
        with open(DATA_FILE, 'r', encoding='utf-8') as f:  # Открываем для чтения
            data = json.load(f)  # Превращаем JSON в Python объект
            
            # Обратная совместимость: добавляем ID для старых записей
            for i, loc in enumerate(data):
                if 'id' not in loc:
                    loc['id'] = i + 1  # ID = позиция в списке + 1
                if 'category' not in loc:
                    loc['category'] = 'other'  # Категория по умолчанию
            return data
    return []  # Если файла нет, возвращаем пустой список


def save_locations(locations):
    """
    Сохраняет список локаций в JSON файл.
    
    Параметры:
    locations - список словарей с локациями
    
    Как работает:
    1. Открывает файл для записи (режим 'w' - перезапись)
    2. Преобразует Python объект в JSON
    3. ensure_ascii=False - сохраняет русские буквы нормально
    4. indent=2 - делает отступы для читаемости
    """
    with open(DATA_FILE, 'w', encoding='utf-8') as f:
        json.dump(locations, f, ensure_ascii=False, indent=2)


